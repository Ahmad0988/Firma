    private void setValues(){
         Emp.setAngetellteId(Integer.parseInt(txtNummber.getText()));
         Emp.setName(txtName.getText());
         Emp.setAdresse(txtAdresse.getText());
         Emp.setGehalt(Double.parseDouble(txtGehalt.getText()));
         
         SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
         
         String EinDate = String.valueOf(f.format(txtEinstellungsDate.getDate()));
         String GebDate = String.valueOf(f.format(txtGeburtsDatum.getDate()));
         
         Emp.setEinstellungsDate(EinDate);
         Emp.setGeburtstag(GebDate);
         String AbteilungName = cbxAbteilung.getSelectedItem().toString();
         String AbteilungNummer = dept.getValueByName(AbteilungName);
         Emp.setAbteilungsId(Integer.parseInt(AbteilungNummer));
    }
    AngestelltPhones Phon = new AngestelltPhones();
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        setValues();
        Emp.add();
        
        Phon.setAngestellteId(Integer.parseInt(txtNummber.getText()));
        for(int i=0 ; i < tblTelefon.getRowCount(); i++){
            Phon.setPhone(tblTelefon.getValueAt(i, 0).toString());
            Phon.add();
        }
        
        newData();
        Emp.getAllRows(tblAngestellte);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        setValues();
        Emp.update();
        
        Phon.setAngestellteId(Integer.parseInt(txtNummber.getText()));
        
        Phon.deleteByAngestellte();
        
        for(int i=0 ; i < tblTelefon.getRowCount(); i++){
            Phon.setPhone(tblTelefon.getValueAt(i, 0).toString());
            Phon.add();
        }
        newData();
        Emp.getAllRows(tblAngestellte);
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(Tools.confirmMesseg("Möchten Sie den Datensatz löschen?")){
            Phon.setAngestellteId(Integer.parseInt(txtNummber.getText()));
            Phon.deleteByAngestellte();
            setValues();
            Emp.delete();
            newData();
            Emp.getAllRows(tblAngestellte);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

