    private void tblAngestellteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAngestellteMouseClicked
        try{
            int row = tblAngestellte.getSelectedRow();
            txtNummber.setText(tblAngestellte.getValueAt(row, 0).toString());
            txtName.setText(tblAngestellte.getValueAt(row, 1).toString());
            txtAdresse.setText(tblAngestellte.getValueAt(row, 2).toString());
            txtGehalt.setText(tblAngestellte.getValueAt(row, 3).toString());
            SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
         
            Date EinDate = f.parse(tblAngestellte.getValueAt(row, 4).toString());
            Date GebDate = f.parse(tblAngestellte.getValueAt(row, 5).toString());
            txtEinstellungsDate.setDate(EinDate);
            txtGeburtsDatum.setDate(GebDate);
            cbxAbteilung.setSelectedItem(tblAngestellte.getValueAt(row, 7).toString());
            
            Phon.setAngestellteId(Integer.parseInt(txtNummber.getText()));
            Phon.getAllRows(tblTelefon);
            
            btnAdd.setEnabled(false);
            btnUpdate.setEnabled(true);
            btnDelete.setEnabled(true);
        }
        catch(Exception ex){
            Tools.messageBox(ex.getMessage());
        }
    }//GEN-LAST:event_tblAngestellteMouseClicked
    String strSuchen;
    private String getSuch(String suchNach,String suchWort){
         strSuchen = String.format("select * from angestellte_data where %s like ", suchNach) ;
         strSuchen += "'%" +suchWort +"%'";
        return strSuchen;
    }
    
    private void btnSuchenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuchenActionPerformed
        if(rdoNummer.isSelected() ){
           getSuch("angestellte_Nummer",txtSuchen.getText());
        }
        else if(rdoName.isSelected()){
            getSuch("name",txtSuchen.getText());
        }
        else if(rdoAdresse.isSelected()){
            getSuch("adresse",txtSuchen.getText());
        }
        else if(rdoGehalt.isSelected()){
            getSuch("gehalt",txtSuchen.getText());
        }
        else if(rdoEinstellung.isSelected()){
            getSuch("einstellung_Date",txtSuchen.getText());
        }
        else if(rdoEinstellung.isSelected()){
            getSuch("Geburtstag",txtSuchen.getText());
        }
        else if(rdoGeburtsDatum.isSelected()){
            getSuch("Geburtstag",txtSuchen.getText());
        }
        else if(rdoAbteilung.isSelected()){
            getSuch("abteilung",txtSuchen.getText());
        }
        
        Emp.getCustomRows(strSuchen, tblAngestellte);
    }//GEN-LAST:event_btnSuchenActionPerformed

