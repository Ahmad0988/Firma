    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String user = txtBenuterName.getText();
        String password = txtPassWort.getText();
       boolean islogged = go.checkUserAndPass(user, password);
        if(islogged){
            this.dispose();
            try {
                Tools.openForm(new frmMain());
            } catch (IOException ex) {
                Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            Tools.messageBox("der Name Des Benutzers oder Passwort ist falsch.");
            txtBenuterName.setText("");
            txtPassWort.setText("");
            txtBenuterName.requestFocus();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

